import * as functions from "firebase-functions";
import OpenAI from "openai";

interface ProblemResult {
  id: number;
  problem: string;
  type: string;
  is_correct: boolean;
  confidence: number;
  notes: string;
}

interface GenerateFeedbackRequest {
  results: ProblemResult[];
  totalProblems: number;
  correctCount: number;
}

interface GenerateFeedbackResponse {
  feedback: string;
  success: boolean;
  error?: string;
}

// Initialize OpenAI client
const getOpenAIClient = () => {
  const apiKey = functions.config().openai?.key;
  if (!apiKey) {
    throw new functions.https.HttpsError(
      "failed-precondition",
      "OpenAI API key not configured"
    );
  }
  return new OpenAI({ apiKey });
};

/**
 * Cloud Function to generate personalized feedback for practice sessions using GPT-4.1 mini
 */
export const generatePracticeFeedback = functions.https.onCall(
  async (
    data: GenerateFeedbackRequest,
    context: functions.https.CallableContext
  ): Promise<GenerateFeedbackResponse> => {
    // Verify authentication
    if (!context.auth) {
      throw new functions.https.HttpsError(
        "unauthenticated",
        "User must be authenticated to generate feedback"
      );
    }

    const { results, totalProblems, correctCount } = data;

    if (!results || !Array.isArray(results)) {
      throw new functions.https.HttpsError(
        "invalid-argument",
        "results must be an array"
      );
    }

    try {
      const openai = getOpenAIClient();

      // Prepare problem summary for the LLM
      const problemSummary = results
        .map((r, idx) => {
          const status = r.is_correct ? "✓ Correct" : "✗ Incorrect";
          const notes = r.notes ? ` (${r.notes})` : "";
          return `${idx + 1}. ${r.problem}\n   ${status}${notes}`;
        })
        .join("\n\n");

      const accuracy = ((correctCount / totalProblems) * 100).toFixed(0);
      const problemType = results[0]?.type === "integral" ? "integrals" : "addition problems";

      const prompt = `You are a friendly math tutor giving quick feedback after a practice session.

**Results:** ${correctCount}/${totalProblems} correct (${accuracy}%) - ${problemType}

**Details:**
${problemSummary}

Write a concise, encouraging feedback message (60-80 words max, 1-2 short paragraphs):
- Start with a positive comment based on performance
- Mention 1-2 specific problems they got right or areas to improve
- End with brief motivational advice

Be warm and supportive. Use simple, natural language. No bullet points.`;

      const completion = await openai.chat.completions.create({
        model: "gpt-4o-mini", // GPT-4.1 mini
        messages: [
          {
            role: "system",
            content: "You are a supportive, encouraging math tutor who gives constructive feedback.",
          },
          {
            role: "user",
            content: prompt,
          },
        ],
        temperature: 0.7,
        max_tokens: 300,
      });

      const feedback = completion.choices[0]?.message?.content || "";

      if (!feedback) {
        throw new Error("No feedback generated by OpenAI");
      }

      return {
        feedback,
        success: true,
      };
    } catch (error) {
      console.error("Feedback generation error:", error);
      return {
        feedback: "",
        success: false,
        error: error instanceof Error ? error.message : "Unknown error occurred",
      };
    }
  }
);

